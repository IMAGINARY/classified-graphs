function graphFromUpperTriangular(M: string) {
  const cy = cytoscape({styleEnabled: true});

  const g = (Number(M.split("  ")[0]) + 2) / 2;
  const N = 2*g - 2;
  const upper = M.split("  ")[1].split(" ").map(Number);

  // to index the upper triangular matrix easily
  var k = 0;

  for (let i = 0; i < 2*g - 2; i += 1) {
    cy.add({ group: 'nodes', data: { id: `N-${i}` } });
    for (let j = 0; j <= i; j += 1) {
      // add the correct amount of edges
      for (let l = 0; l < upper[k]; l += 1) {
        cy.add({
          group: 'edges',
          data: {
            source: cy.nodes()[j].id(),
            target: cy.nodes()[i].id(),
            id: `E-${j}-${i}-${l}`,
          },
        });
      }

      // increment counter for upper triangular matrix indexing
      k++;
    }
  }
  cy.style().selector("edge").style("curve-style", "bezier").update();
  cy.layout({ name: 'circle', radius: 200 }).run();
  cy.zoom(1);
  cy.panBy({ x: 600, y: 500 });

  return cy;
}

