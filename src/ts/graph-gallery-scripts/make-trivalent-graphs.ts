function makeFile(cy: Core, filename: string) {
  const json = cy.json();
  const jsonString = JSON.stringify(json, null, 4);

  fs.writeFile(filename, jsonString, {}, (err) => {
    if (err) throw err;
    console.log(`Saved file ${filename}`);
  });
}

